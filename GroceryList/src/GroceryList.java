/* This is a for each in Java. It just used the keyword for */

public class GroceryList {
    public static void main(String[] args) {
        String[] myGroceryList = {"greek yogurt", "apples", "turkey", "spinach", "lettuce", "mayonnaise"};

        for(String x : myGroceryList){
            System.out.println(x);
        }
    }

}
